import com.example.detailscreen.data.Item



@Composable
fun ItemListItem(item: Item, naviateToProfile: (Item) -> Unit) {
    Card(
        modifier = Modifier
            .padding(horizontal = 8.dp, vertical = 8.dp)
            .fillMaxWidth(),
        shape = RoundedCornerShape(corner = CornerSize(16.dp)),
    ) {
        Row(
            Modifier.clickable { naviateToProfile(item) }
        ) {
            ItemImage(item = item)
            Column(
                modifier = Modifier
                    .padding(16.dp)
                    .fillMaxWidth()
                    .align(Alignment.CenterVertically)
            ) {
                Text(text = item.title, style = typography.headlineSmall)
                Text(text = "VIEW DETAIL", style = typography.titleSmall)
            }
        }
    }
}

@Composable
private fun ItemImage(item: Item) {
    Image(
        painter = painterResource(id = item.itemImageId),
        contentDescription = null,
        contentScale = ContentScale.Crop,
        modifier = Modifier
            .padding(8.dp)
            .size(84.dp)
            .clip(RoundedCornerShape(corner = CornerSize(16.dp)))
    )
}